from enum import Enum


class ThreatType(Enum):
    """Threat classification types."""
    BPLA = "–ë–ü–õ–ê"
    RECON = "–†–∞–∑–≤–µ–¥–∫–∞"
    ROCKET = "–†–∞–∫–µ—Ç–∞"
    KAB = "–ö–ê–ë"
    BALLISTIC = "–ë–∞–ª—ñ—Å—Ç–∏–∫–∞"
    EXPLOSION = "–í–∏–±—É—Ö–∏"
    LAUNCH = "–ü—É—Å–∫"
    UNKNOWN = "–ù–µ–≤—ñ–¥–æ–º–æ"
    
    @classmethod
    def from_string(cls, s: str) -> 'ThreatType':
        """Parse threat type from various string formats."""
        s_lower = s.lower()
        
        if any(x in s_lower for x in ['—Ä–æ–∑–≤—ñ–¥–∫', '—Ä–æ–∑–≤—ñ–¥—É–≤–∞–ª—å–Ω', '—Ä–æ–∑–≤—ñ–¥—É–≤–∞–ª—å–Ω–∏–π –±–ø–ª–∞', '–±–ø–ª–∞-—Ä–æ–∑–≤—ñ–¥–Ω–∏–∫']):
            return cls.RECON
        if any(x in s_lower for x in ['–±–ø–ª–∞', '—à–∞—Ö–µ–¥', '–≥–µ—Ä–∞–Ω—å', '–º–æ–ø–µ–¥', '–±–∞–ª–∞–ª–∞–π–∫', 'drone']):
            return cls.BPLA
        if any(x in s_lower for x in ['—Ä–∞–∫–µ—Ç', '–∫–∞–ª—ñ–±—Ä', 'cruise', '–∫—Ä–∏–ª–∞—Ç']):
            return cls.ROCKET
        if any(x in s_lower for x in ['–∫–∞–±', 'kab', '–±–æ–º–±']):
            return cls.KAB
        if any(x in s_lower for x in ['–±–∞–ª—ñ—Å—Ç', 'ballistic', 'iskander']):
            return cls.BALLISTIC
        if any(x in s_lower for x in ['–≤–∏–±—É—Ö', 'explosi', 'üí•']):
            return cls.EXPLOSION
        if any(x in s_lower for x in ['–ø—É—Å–∫', '–ø—É—Å–∫–∏']):
            return cls.LAUNCH
        
        return cls.UNKNOWN

