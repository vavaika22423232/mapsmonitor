    # -–∫–∏ -> -–∫–∞ (–®–æ—Å—Ç–∫–∏ -> –®–æ—Å—Ç–∫–∞, –û—Ö—Ç–∏—Ä–∫–∏ -> –û—Ö—Ç–∏—Ä–∫–∞)
    # -—ñ–≤ -> -—ñ–≤ (–•–∞—Ä–∫—ñ–≤ -> –•–∞—Ä–∫—ñ–≤ - –±–µ–∑ –∑–º—ñ–Ω)
